package model;

public class Gusto {
    private int idGusto;
    private String nomeGusto;


    public int getIdGusto() {
        return idGusto;
    }

    public void setIdGusto(int idGusto) {
        this.idGusto = idGusto;
    }

    public String getNomeGusto() {
        return nomeGusto;
    }

    public void setNome(String nomeGusto) {
        this.nomeGusto = nomeGusto;
    }
}
