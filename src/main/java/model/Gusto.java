package model;

public class Gusto {
    private int idGusto;
    private String nome;


    public int getIdGusto() {
        return idGusto;
    }

    public void setIdGusto(int idGusto) {
        this.idGusto = idGusto;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
